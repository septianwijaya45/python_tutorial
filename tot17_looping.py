# Perulangan (loop)

"""
for kondisi:
    aksi
"""

# dengan range
angka2_range = range(5)
for i in angka2_range:
    print(f"range angka ke-{i}")

for i in range(1, 10):
    print(i)

# dengan list
angka2 = [0,1,2,3,4] # datatype list
print(angka2)

for i in angka2:
    print(f"i now => {i}")

# string
data_string = "aku aan"

for i in data_string:
    print(f"huruf =>{i}")